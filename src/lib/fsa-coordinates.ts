/**
 * Get coordinates for a Canadian postal code using FSA lookup
 * @param postalCode Full postal code (e.g., "M5V 2H1") or FSA (e.g., "M5V")
 * @returns Coordinates or null if FSA not found
 */
export function getFSACoordinates(postalCode: string): FSACoordinates | null {
  // Extract FSA (first 3 characters)
  const fsa = postalCode.replace(/\s+/g, "").substring(0, 3).toUpperCase();
  return FSA_COORDINATES[fsa] || null;
}

/**
 * Check if a postal code's FSA is in Ontario
 */
export function isOntarioPostalCode(postalCode: string): boolean {
  const fsa = postalCode.replace(/\s+/g, "").substring(0, 3).toUpperCase();
  const firstChar = fsa.charAt(0);
  // Ontario FSAs start with K, L, M, N, or P
  return ["K", "L", "M", "N", "P"].includes(firstChar);
}
